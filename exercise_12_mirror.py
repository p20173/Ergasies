#Ζητάω από τον χρήστη να μου δώσει την διεύθυνση του αρχείου του
file = input('Give file path: ')
#Ανοίγω το αρχείο
str = open(file, 'r').read()
#Δημιουργώ μια λίστα στην οποία θα εισάγω τους χαρακτήρες από τους οποίους τα ascii values τους είναι συμπληρωματικ΄ο του 128
mirror = []
for i in str:
    mirror.append(chr(128-ord(i)))
#Αντιστρέφω την λίστα
mirror.reverse()
#Ενώνω τα στοιχεία της λίστας ώστε να εμφανίζονται κολλητά
a = "".join(mirror)
print(a)
